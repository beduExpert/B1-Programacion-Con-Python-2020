#Se puede definir números como cadenas si se encierran en comillas
numero1 = "100"
numero2 = "3.14159"
print(type(numero1))

#Para convertir a entero 
entero = int(numero1)
print(type(entero))

#Para convertir a flotante
flotante = float(numero2)
print(type(flotante))

#También se puede convertir un número a cadena de texto
num = 300
cadena = str(num)
print(type(cadena))
